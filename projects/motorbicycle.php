    <p>I did a little research on the net then sketched out how I would try and build it. That weekend I went to the fleamarket and found an old bike that needed a tire for $30. It would work. I sketched out on a piece of paper a rough design, pulled out my sawzaw and stick welder, and got busy!
    </p><p>
    Since the plastic case of the chainsaw holds the crankcase and cylinder together, the whole engine came apart as soon as I took it out of the case. So I had to build a custom motor mount and buy some longer bolts to hold it all together. The hardest part was probobaly the custom motor mount. The plastic case of a chainsaw also holds the magneto in place (crucial for proper ignition timing). I had to carefully mark it's position to the flywheel and build it into the motor mount. Also it was important to insulate the magento with fiber washers since grounding the it kills the igntion.</p>
