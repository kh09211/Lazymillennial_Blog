    <p>It all started in 2012 when my dad dropped off a broken chainsaw at my house. He said, he paid a guy to fix it and it never really worked right after. Maybe I could fix it and have it. It ended up being a 36cc craftsman chainsaw which was the same as the 36cc poulan chainsaw. Well I found out the issue. A leaking crankcase which is crucial to a 2 stroke engine for proper functionality. Anyway, I got an idea somewhere to try and use it to build a motorized bicycle...</p>

