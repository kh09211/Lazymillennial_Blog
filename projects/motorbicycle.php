    <p> The only major problem that I kept having was the crankcase seal kept leaking. The exact problem that broke the original chainsaw in the first place. I ended up taking it apart about 4 or 5 times and trying different sealants. Nothing seemed to work (evidenly the permatex I was using was expired). I ended up using orange hi-temp RTV permatex which is not rated for gasoline, but it held up just fine.</p>
