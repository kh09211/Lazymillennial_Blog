    <p>After running several gallons of fuel, I calculated that the bike gets nearly 100 miles per gallon and according to the GPS zip tied to the handlebars, it has a top speed of 34 mph. Fast enough that I needed a helmet. On the street, people looked at me funny when they saw a bicycle passing them on the avenue. After months of riding this thing, I let the little brother of a friend borrow it. He painted the bike then entered it into the county fair where it won 2nd place for FFA projects. Not exactly ethical I know, but I'm still proud haha. 
    </p><p>
    Now for the bad part.... He brought the bike back back wrecked. I mended it the best I could but the motor mount had broken. I eventually threw it in the scrap pile. It wasn't a bad little device for a project less than $100. This was my first motorized 2 wheel vehicle and it got me hooked real good. Since then I've owned 9 different motorcycles over the years and am still in love with 2 wheels today! Thanks for reading and feel free to comment below!</p>
