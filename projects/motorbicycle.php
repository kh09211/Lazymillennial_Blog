    <p>At first, I designed a spring loaded scisor clutch to start the engine but I kept having problems with the cables streching. I couldnt figure out how to start the thing with the motor mounted on the tire. My dad got on it and started petaling and the thing just fired up! You petal to start, and it stalls the engine when you stop. Works awesome! I connected the throttle of the carburetor to one of the bike's gear selectors. I didn't need the extra gears and it actually doubled as a slightly dangerous cruise control. The Spring that holds the motor against the tire came from a trampoline.
    </p><p>
    I could not find an M8 1.0 reverse thread nut to fit the driveshaft anywhere in town or on the internet so what I did was disassemble the clutch and cut the center out of it. I was going to weld a nut to it however I left it in an oval shape so I could tighten it with channelock pliers. Hmm seemed to work! Next! The socket is a 7/8" 3/8 drive drilled out slightly bigger to slip over the driveshaft. A washer sits on the driveshaft shoulder to the socket from sliding all the way into the cyclinder jug. </p>
