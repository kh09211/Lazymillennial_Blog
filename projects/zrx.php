    <p>I finally had it! My first big bore 1000cc+ motorcycle. Sure I noticed it had a few things rigged here and there, but I could ride it home and I could probably fix it up fine! That night after removing the tank and airbox, I started panicking as I noticed problem after problem with this bike. I decided to take it out on a short little ride and suddenly heard a clack clack clack BANG as the bike threw itself out of gear and revved up into a false neutral. What have I gotten myself into?</p>

    <p>The loud bang I heard, I learned was the symptom of a bent shift fork. A mortal problem for a motorcycle in which to repair, the engine normally must be removed from the frame and nearly completely disassembled. Not a common job for a novice mechanic and a very costly one to be done by a shop. Well, I was $2000 in the hole on this bike and if I was ever going to get any of my money back out of it, I had to dig in and fix this thing. That night, my friend came over and we began removing the engine from the frame. It was surprisingly lightweight as we lifted the engine up and strapped it to my garage workbench.</p>

