    <p>So with the engine back together mostly, I decided, well since it is out of the frame I might as well upgrade some parts. I read online that the ZX11 ninja uses the same engine and therefore the camshafts and valve springs could be swapped out. Cool, I bought some on eBay and used my air compressor to keep the valves seated while I swapped out springs.</p>

    <p>Since I had new cams, I had to re-jet the carburetors. And since I was re-jetting I might as well rebuild them.... This project started snowballing. I then ditched the stock airbox for pod filters. Well if the engine is going to breath better with cams and carb mods, I might as well upgrade the exhaust system....</p>

    <p>So headers for this bike are hard to find. But I found a Yoshimira header on eBay for a GPZ1100. A different bike that used the same engine. You can see the difference in the exhaust routing of the new header verses the old one that is straight. Since the exit was different, I had to build a pipe that could connect the new header to the old exhaust muffler which was a Kerker brand can. I used pieces of 2.5" pipe that I cut into pie slices to carefully link the two pieces together. After paint, I was happy with the result and it sounded AMAZING!</p>

