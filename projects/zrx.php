    <p>The final upgrade to the bike was a set of sprockets. 1 gear smaller on the front and two gears larger on the rear. Finally I synchronized the carburetors using a special home made tool. A board with sections of tubing linked together and to the carburetor. The tubes are filled with automatic transmission fluid and when the intake of each cylinder fights for air, they pull on the fluid in different ways allowing you to tune the carburetors to pull at an equal rate. The result is a very smooth running engine.</p>

