    <p>When I got done with the bike, I'm not going to lie. It was too much for me. The bike would easily pull the front wheel up in the air in 2nd gear with just a light snap of the throttle. It was more than a bit scary to ride. But at the same time it ran soooo well. I decided not long after this repair/build, that I wanted to change gears and live life in an RV for a while to finally see the country. For that reason, I ended up selling the bike. I got almost what I put into it money wise which made me happy enough since broken bikes are worth just a fraction. I learned a great deal from this project tech wise, and most importantly, I learned to be VERY careful about buying used motorcycles. Sometimes just paying a little bit more can save you a lot of headache and dollars in the long run. Duly noted.</p>

