    <p>I found a service manual online and started getting my hands greasy. It was A LOT of work to do. Once I split the crankshaft case apart, I found the issue. Sure enough, it was a bent shift fork. Not one but two of them. You can tell which are bad because when they bend, they start wearing circular grooves into the arm section which results in a sort of "smiley face" type of witness mark. I found some used parts on eBay and bought them. Swapping them out was not that fun, but after a lot of hours, the engine was finally back in one piece.</p>

