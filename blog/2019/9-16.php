    <p class="text-left">9/16/2019</p>
    <p>Driving up to the event center (photo above), we begin to see a long line of people at least an 1/8th of a mile long from the front door and wrapping around the side of the building. I was on my lunch break from my current dead end job hoping to get a chance to apply for Kumho Tire, a factory that was opening up in another city about 20 minutes from my home town. We parked then followed the line to its end. Evidently the convention room was already full and new people could only enter once others left.</p>

    <p>After an hour of waiting, we finally entered the building. Another line continued through the lobby into the convention area. I was used to this by now. It was not my first job fair with this type of volume. More waiting... Once inside the main area, it was total chaos. Most of the employers recruiting were restaurants such as Waffle House and Apple Bees. They were hiring waitresses and cooks. A step down in pay from my current job as a tech crawling under dirty diesel trucks. I headed to the Kumho tire line where I waited with my father for another 20 or 30 minutes.</p>

    <img src="9-16/9-16-2.JPG" alt="job fair inside" class="img-fluid border border-dark">
    <br><br>
    <p>At last I was at the front of the line for Kumho Tire! I had my best suit on and a folder full of freshly printed and restructured resumes on ivory paper complete with the watermark stamp. I greet the tired lady and open my folder to hand her one of my resumes. She stops me and then in a tired monotone voice she says, "We're not accepting any new resumes right now. Put your name on this list please." I wrote my name down, then she hands me a small piece of paper. On it is the website to the career page of KumhoTire.com. She say "We are only accepting online applications right now. But we have your name on the list so we know you were here. NEXT!"</p>

    <p>I look around for any other possible employers. Nothing but restaurants and even they had long lines of people in front of their tables. Another wasted trip. I knew I wouldn't ever hear from Kumho Tire but I applied anyway as soon as I got home. The desperate looks on the people's faces there who were either in the same boat or worse than me was telling. Even in my county where the unemployment rate was fairly low, the affects of the Great Recession were still looming even in 2015. At least my dad got to see how bad things were for himself. That year I put in dozens more online applications without a single interview or even a phone call. I knew I had to change my life in a more drastic way. Maybe I should buy an RV and just leave this place...</p>
