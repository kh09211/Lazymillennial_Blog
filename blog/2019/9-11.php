    <p>It was a normal morning at my high school and I was sitting in my first period classroom. It was Civics class with Mrs. Duncan. All of a sudden an anouncement was made over the intercom. "Attention teachers, please turn your televisions on. A plane has just hit the world trade center." We turned the TV on and sure enough there was the smoke coming out of the World Trade Center. They were talking about it being an accident and then shortly after, a huge fireball erupted from the other tower. It was total confusion. Within just a short time later, we saw camera footage of smoke coming from the pentagon.</p>
