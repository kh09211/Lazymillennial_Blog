    <p>There was no mistake at that point what was going on. The USA was under attack. Nobody knew who or why at the time and the speculations started. Was it the Chineese? Islamic radicalist? Homegrown terrorist? Of course we would later learn as Bin Ladin openly took credit for the attacks.</p><p>
    Throughout the rest of the day, we had practically no school work. We went from class to class with our eyes glued to the television screens. Some classes we had discussions about what this meant and how we felt about it. Not long before the end of the day, a second anouncement came over the intercom. They said "Robins Air Force Base is now on Defcon Delta." Robins AFB was the local base and the largest employer of our little town. This meant that nobody could leave or get on to the military base. They said students who could not get home could wait at the school. It was not until after 8pm that the parents could leave the bas to pick up their children.</p>
