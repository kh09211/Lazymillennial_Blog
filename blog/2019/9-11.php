    <p class="text-left">9/11/2019</p>
    <p>Since it's September 11th and I finally have my website coded well enough to start uploading content, I thought that I would share a typical 9/11 story. Something similar to the JFK assasination where we tell others were we were and what we were doing when we heard the news. I know it's cliche to say this but I really do remember it almost like it was yesterday. It's hard to imagine that it has already been 18 years ago to this day....</p>
    <br>
