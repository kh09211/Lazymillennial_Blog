    <p>Looking back after writing this article, it does seems to be a long time ago. Something that rings very true is what my Social Studies teacher said to us that day. He said "Look kidos, the 90s are over. You grew up in a time of a booming economy where everything was great. There is now going to be a war and your lives are going to be tough from here on out." I actually scoffed at this man's comment. But he was EXACTLY right on the money. Wars and a brutal economic decade lay shortly ahead for us millennials.</p>

